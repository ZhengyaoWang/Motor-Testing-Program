void TIM3_IRQHandler(void)
{
	if (TIM_GetITStatus(TIM3, TIM_IT_Update) == SET)
	{
		if(stpStatus == 0)
		{
			GPIO_ResetBits(Stp_PORT, Stp_PIN);		//Stp置低电平
		}
		else
		{
			GPIO_SetBits(Stp_PORT, Stp_PIN);			//Stp置高电平
		}
		stpStatus ^= 1;
		
		TIM_ClearITPendingBit(TIM3, TIM_IT_Update);
	}
}

